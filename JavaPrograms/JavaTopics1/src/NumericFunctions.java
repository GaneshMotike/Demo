
public class NumericFunctions {

	public static void main(String[] args) 
	{
		System.out.println(Math.PI);
		System.out.println(Math.abs(+10.26));
		System.out.println(Math.abs(-10.26));
		System.out.println(Math.floor(10.25));
		System.out.println(Math.floor(10.99));
		System.out.println(Math.ceil(10.25));
		System.out.println(Math.ceil(10.99));
		System.out.println(Math.round(10.4));
		System.out.println(Math.round(10.8));
		System.out.println(Math.sin(45));
		System.out.println(Math.cos(45));
		System.out.println(Math.tan(45));
		System.out.println(Math.log(45));
		System.out.println(Math.sqrt(810));
		System.out.println(Math.max(15, 9));
		System.out.println(Math.min(15, 6));
		

	}

}
